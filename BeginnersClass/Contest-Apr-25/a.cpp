#include <iostream>

using namespace std;

static const string endW = "\n";
void solution()
{
    long long int n;
    cin >> n;

    int rel[n];

    for (int i = 0; i < n; i++)
        cin >> rel[i];
};

int main()
{
    long long int _;
    cin >> _;
    while (_--)
    {
        solution();
        cout << endW;
    }

    return 0;
}